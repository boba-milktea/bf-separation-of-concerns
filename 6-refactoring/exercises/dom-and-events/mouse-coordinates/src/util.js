/**
 * format the position x and position y
 *
 * @param {number} xValue the x postion
 * @param {number} yValue the y position
 * @returns {string} a string with x and y position
 */

const formatCoordinates = (xValue, yValue) => 'X: ' + xValue + '\nY: ' + yValue;

export default formatCoordinates;
